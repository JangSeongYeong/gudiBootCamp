
public class Methods {
		
		// 반환타입 O, 매개변수 O, 리턴 값 O
		String 토스트기 (String 빵) {
			return "구운" + 빵;
		}
		
		// 반환타입 O, 매개변수 X
		String 번호표기계() {
			return "번호표";
		}
		
		// 반환타입 X, 매개변수 O
		void 저금통(int 동전) {
			System.out.println(동전 + " 저금통");
		}

		// 반환타입 X, 매개변수 x
		void 호출벨() {
			System.out.println("호출");
		}

		// 리턴 값이 있으면 리턴 타입을 준비해줘야 한다.
}
